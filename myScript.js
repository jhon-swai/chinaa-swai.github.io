function hideNavigationBarFunction(){
  var smallNav =document.getElementById("smallNav");
  if(smallNav.className.indexOf("w3-show")== -1){
    smallNav.className += " w3-show";
  }
  else{
    smallNav.className = smallNav.className.replace(" w3-show","")
  }
}
